import "../../../styles/Drawer.css";
import React from 'react'
import { Add, Close, Storage } from '@mui/icons-material'
import { Button, IconButton, TextField, Typography } from '@mui/material'
import { RippleEffect } from "../../../utilities/ripple";
import { useForm } from "react-hook-form";
import axiosInstance from "../../../config/axiosInstance";
import { useDispatch, useSelector } from "react-redux";
import { showSnackbar } from "../../../redux/slices/snackbar";

const AddEditStorageLocation = (props) => {

    const user = useSelector((state) => state.auth.user)
    const dispatch = useDispatch();

    const { register, handleSubmit, reset, formState: { errors } } = useForm({
        defaultValues: {
            storageCode: "",
            storageDesc: "",
            status: "Active",
        },
        mode: "onBlur",
        reValidateMode: "onChange",
    });

    const onSubmit = async (data) => {
        // console.log(user)
        // console.log(data);
        // data.createdby = user?._id;
        // data.status = "Active";
        
        try {
            const result = await axiosInstance.post(`/strg/create`, data).then(res => res.data)
            
            if(result.statuscode === 201){
                props.onClose(); // Close the drawer
                dispatch(showSnackbar({ message: result.message, severity: 'success', }));
            }

        } catch (error) {
            const message = error.response ? error.response.data.message : error.message;
            dispatch(showSnackbar({ message, severity: 'error', }));
        }
    };

    const handleReset = (e) => {
        RippleEffect(e)
        reset();
    };

    return (
        <section className="drawer-container">
            <div className="drawer-header">
                <Typography className="title" color="primary">
                    <Storage color="primary" style={{ fontSize: "2rem", margin: "-10px 0" }} /> ADD STORAGE LOCATION
                </Typography>
                <IconButton onClick={() => props.onClose()}> <Close color="error" width={300} /> </IconButton>
            </div>
            <form className="drawer-form" onSubmit={handleSubmit(onSubmit)}>
                <div className="input-container"> 
                    <TextField label="Storage Location Code" variant="filled" {...register("storageCode", { required: "Storage Location Code is required" })} 
                    error={!!errors.storageCode} helperText={errors.storageCode?.message} fullWidth /> 
                </div>
                <div className="input-container"> 
                    <TextField  label="Storage Location Description"  variant="filled" {...register("storageDesc")} 
                    error={!!errors.storageDesc} helperText={errors.storageDesc?.message} fullWidth /> 
                </div>

                <div className="action-buttons">
                    <div style={{padding: "1rem 2rem", display: "flex", alignItems: "center", gap: "1rem"}}>
                        <Button type="submit" variant="contained" size="large">
                            ADD <Add style={{ margin: "-3px 0 0 4px" }} />
                        </Button>
                        
                        <div className="reset-button" onClick={handleReset}> Reset </div>
                    </div>
                </div>
            </form>
        </section>
    )
}

export default AddEditStorageLocation